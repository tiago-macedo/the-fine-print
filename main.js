function make_super(text) {
	text = text.split('')
	for (key in text) {
		text[key] = super_dict[text[key]]
	}
	return text.join('')
}

function make_sub(text) {
	text = text.split('')
	for (key in text) {
		text[key] = sub_dict[text[key]]
	}
	return text.join('')
}

var vm = new Vue({
	el: 'main',
	data: {
		input: null,
		type: 'sub',
	},
	computed: {
		converted: function() {
			if (this.input) {
				if (this.type === 'sub') { return make_sub(this.input) }
				else if (this.type === 'sup') { return make_super(this.input) }
			}
			return null
		}
	}
})
